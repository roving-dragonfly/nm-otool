# include "nm.h"

/*
** Endianess checker, from configure
** 0 = big, 1 = little
*/

uint8_t	endianess(void)
{
	volatile uint32_t i;

	i = 0x01234567;
	return ((*((uint8_t*)(&i))) == 0x67);
}
